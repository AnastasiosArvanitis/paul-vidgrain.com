     <p>Toutes les techniques  et tous les matériaux m' intéressent,
      tant en peinture qu'en sculpture.</p>
      <p>En peinture, mon thème préféré est le bord de la mer.<br>
        Je pars d'un croquis ou d'une aquarelle sur nature que je
        transcris en peinture , à l'atelier,<br> ajoutant  souvent des personnages.</p>
      <p>En sculpture, je travaille la terre cuite, le bois, le plâtre ou le ciment
         et le plus souvent je patine.</p>
      <p>Je puise mon inspiration dans les petits moments de la vie
        de tous les jours. <br>Les thèmes sont variés :
        personnages seuls ou en couple,enfants,<br> animaux en mouvements ou statiques.</p>
      <p>Mes sculptures sont souvent lisses car j'aime qu'elles soient
        douces à l'oeil ou au toucher".</p>
